import java.util.*;

class pattern {
  public static void main(String args[])
  {
    Scanner scan = new Scanner(System.in);
    int m = scan.nextInt();
    int n = scan.nextInt();
    for(int i=0;i<m;i++)
    {
       for(int j=0;j<n;j++)
       {
         System.out.print("*");
       }
       System.out.println();
    }
  }
   
     
}
